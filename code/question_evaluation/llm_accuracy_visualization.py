import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

# Define the data
data = {
    'Large Language Model': ['GPT-3.5-Turbo', 'GPT-4o', 'Meditron-70B', 'Llama-3-8B', 'Llama-3-70B', 'Mistral-7B', 'Mixtral-8x7B', 'PMC-Llama'],
    'Accuracy': [70.0, 74.4, 44.5, 64.8, 73.8, 56.8, 68.2, 51.9]
}

# Convert the data into a DataFrame
df = pd.DataFrame(data)

# Define a custom palette with more distinct shades
custom_palette = {
    'GPT-3.5-Turbo': '#1f77b4',         # Blue
    'GPT-4o': '#aec7e8',  # Lighter Blue
    'Meditron-70B': (109/255, 93/255, 229/255),   # Purple
    'Llama-3-8B': '#2ca02c',     # Green
    'Llama-3-70B': '#98df8a',    # Lighter Green
    'Mistral-7B': '#d62728',     # Red
    'Mixtral-8x7B': '#ff9896',   # Lighter Red
    'PMC-Llama': '#ffbb78'       # Lighter Orange
}

# Create a bar plot using seaborn with the custom palette
plt.figure(figsize=(14, 6))  # Width x Height in inches
bar_plot = sns.barplot(x='Large Language Model', y='Accuracy', data=df, palette=custom_palette)
plt.xticks(rotation=30, fontsize=24)  # Rotate x-axis labels and set font size
plt.yticks(fontsize=24)  # Set font size for y-axis ticks
plt.ylabel('Accuracy', fontsize=24)  # Set font size for y-axis label
plt.xlabel('', fontsize=24)  # Set font size for x-axis label
plt.ylim(0, 100)  # Set y-axis limits from 0 to 100

# Adding the accuracy labels on top of each bar
for index, row in df.iterrows():
    bar_plot.text(index, row['Accuracy'] + 1, f"{row['Accuracy']}%", color='black', ha="center", fontsize=18)

# Show the plot
plt.show()